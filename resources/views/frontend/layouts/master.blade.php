        let product_id = '';

        function submitForm(id) {
            this.product_id = "";
            let quantity = $('#cart-qty-' + id).val();
            let product_id = id;


            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });


            $.ajax({
                url: '/add-to-cart',
                type: 'POST',
                data: {
                    'quantity': quantity,
                    'product_id': product_id
                },
                success(res) {
                    console.log(res)

                    if (res.status == 300) {
                        window.location.replace('/login');
